public class ToManyMistakesException extends Throwable {

    public ToManyMistakesException() {
    }

    public ToManyMistakesException(String s) {
        super(s);
    }

    public ToManyMistakesException(String message, Throwable cause) {
        super(message, cause);
    }

    public ToManyMistakesException(Throwable cause) {
        super(cause);
    }
}
